// -- This is a parent command --
Cypress.Commands.add("openDrawerCommand", () => {
  cy.get('#stockPage')
    .find('button')
    .click()
})

Cypress.Commands.add("closeDrawerCommand", () => {
  cy.get('.MuiGrid-container > :nth-child(2) > .MuiButtonBase-root')
    .click()
})
